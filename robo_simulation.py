import numpy as np
import random
import matplotlib.pyplot as plt
import heapq

class CaptureTheFlag:
    def __init__(self):
        plt.ion()  # Enable interactive mode
        self.grid_size = 10
        self.flag_pos = (5, 5)
        self.drop_zone = (0, 9)
        self.attacker_pos = (0, 0)
        self.defender_pos = (1, 6)
        self.attacker_has_flag = False
        self.moves = {"N": (-1, 0), "S": (1, 0), "E": (0, 1), "W": (0, -1),
                      "NE": (-1, 1), "NW": (-1, -1), "SE": (1, 1), "SW": (1, -1)}
        self.player_role = None
        self.max_steps = 100
        self.current_step = 0
        self.obstacle=(3,3)
        self.patrol= (random.randint(0,9),random.randint(0,9))

    def is_valid_move(self, pos):
        return 0 <= pos[0] < self.grid_size and 0 <= pos[1] < self.grid_size and pos != self.obstacle and pos != self.patrol

    def heuristic(self, a, b):
        return abs(a[0] - b[0]) + abs(a[1] - b[1])

    def astar(self, start, goal):
        open_set = []
        heapq.heappush(open_set, (0, start))
        came_from = {}
        g_score = {start: 0}
        f_score = {start: self.heuristic(start, goal)}

        while open_set:
            current = heapq.heappop(open_set)[1]
            if current == goal:
                path = []
                while current in came_from:
                    path.append(current)
                    current = came_from[current]
                path.append(start)
                path.reverse()
                return path

            for move in self.moves.values():
                neighbor = (current[0] + move[0], current[1] + move[1])
                if self.is_valid_move(neighbor):
                    tentative_g_score = g_score.get(current, float('inf')) + 1
                    if tentative_g_score < g_score.get(neighbor, float('inf')):
                        came_from[neighbor] = current
                        g_score[neighbor] = tentative_g_score
                        f_score[neighbor] = tentative_g_score + self.heuristic(neighbor, goal)
                        heapq.heappush(open_set, (f_score[neighbor], neighbor))
        return None

    def move_attacker(self, direction=None):
        if direction:
            new_pos = (self.attacker_pos[0] + self.moves[direction][0],
                       self.attacker_pos[1] + self.moves[direction][1])
            if self.is_valid_move(new_pos):
                self.attacker_pos = new_pos
        else:
            target = self.drop_zone if self.attacker_has_flag else self.flag_pos
            path = self.astar(self.attacker_pos, target)
            if path and len(path) > 1:
                self.attacker_pos = path[1]

        if self.attacker_pos == self.flag_pos:
            self.attacker_has_flag = True
            print("Attacker has captured the flag!")

        if self.attacker_has_flag and self.attacker_pos == self.drop_zone:
            print("Attacker wins! Flag delivered to drop zone.")
            return True

        return False

    def move_defender(self, direction=None):
        if direction:
            new_pos = (self.defender_pos[0] + self.moves[direction][0],
                       self.defender_pos[1] + self.moves[direction][1])
            if self.is_valid_move(new_pos):
                self.defender_pos = new_pos
        else:
            path = self.astar(self.defender_pos, self.attacker_pos)
            if path and len(path) > 1:
                self.defender_pos = path[1]

        dx = abs(self.attacker_pos[0] - self.defender_pos[0])
        dy = abs(self.attacker_pos[1] - self.defender_pos[1])
        if dx <= 1 and dy <= 1:
            print("Defender wins! Attacker captured.")
            return True

        return False

    def move_patrol(self):
        directions = list(self.moves.values())
        random.shuffle(directions)  # Random movement
        for move in directions:
            new_pos = (self.patrol[0] + move[0], self.patrol[1] + move[1])
            if self.is_valid_move(new_pos):
                self.patrol = new_pos
                break

    def plot_grid(self):
        plt.clf()
        plt.figure(1, figsize=(6, 6))
        plt.xlim(-1, self.grid_size)
        plt.ylim(-1, self.grid_size)
        plt.grid(True)

        plt.scatter(self.flag_pos[1], self.flag_pos[0], marker='*', color='yellow', s=200, label='Flag')
        plt.scatter(self.drop_zone[1], self.drop_zone[0], marker='D', color='blue', s=100, label='Drop Zone')
        plt.scatter(self.attacker_pos[1], self.attacker_pos[0], marker='o', color='red', s=150, label='Attacker')
        plt.scatter(self.defender_pos[1], self.defender_pos[0], marker='s', color='green', s=150, label='Defender')
        plt.scatter(self.obstacle[1], self.obstacle[0], marker='X', color='black', s=150, label='Obstacle')
        plt.scatter(self.patrol[1], self.patrol[0], marker='P', color='purple', s=150, label='Patrol')


        plt.legend()
        plt.gca().invert_yaxis()
        plt.draw()
        plt.pause(0.1)




    def play_game(self):
        print("Starting Capture the Flag game...")
        while self.player_role not in ['attacker', 'defender']:
            self.player_role = input("Do you want to play as attacker or defender? ").lower()

        while self.current_step < self.max_steps:
            self.current_step += 1
            print(f"\n--- Step {self.current_step} ---")
            self.plot_grid()

            # Attacker move
            attacker_won = False
            if self.player_role == 'attacker':
                while True:
                    direction = input("Enter attacker move (N/S/E/W/NE/NW/SE/SW): ").upper()
                    if direction in self.moves:
                        attacker_won = self.move_attacker(direction)
                        break
                    print("Invalid move! Try again.")
            else:
                attacker_won = self.move_attacker()

            if attacker_won:
                break

            # Defender move
            defender_won = False
            if self.player_role == 'defender':
                while True:
                    direction = input("Enter defender move (N/S/E/W/NE/NW/SE/SW): ").upper()
                    if direction in self.moves:
                        defender_won = self.move_defender(direction)
                        break
                    print("Invalid move! Try again.")
            else:
                defender_won = self.move_defender()

            if defender_won:
                break

            self.move_patrol()

        if self.current_step >= self.max_steps:
            print("\nGame ended in a draw!")




        plt.ioff()
        plt.show()

if __name__ == "__main__":
    game = CaptureTheFlag()
    game.play_game()